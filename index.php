      <div class='content'>
        <h2 id='save-with-mstable'>Try: Save with mStable 🧮</h2>
				<p class='center'>Deposit stablecoins into mStable's high-interest savings contract<p>
        <ol>
					<li>Buy some USDC in the amount you want to save on <a href='https://www.coinbase.com/price/usdc' target='_blank'>Coinbase</a> (no fees!)</li>
					<li>Send the USDC to your Ethereum address (need some ETH too)</li>
					<li>Go to <a href='https://app.mstable.org/#/save' target='_blank'>mStable.org/save</a>, click Connect and choose MetaMask</li>
					<li>Click imUSD (stands for interest-bearing mUSD)</li>
					<li>Choose USDC in the deposit dropdown of popup, then type in amount to deposit</li>
					<li>Click to Deposit. May have to approve a first transaction to enable USDC</li>
					<li>Done! Holding imUSD means you're earning interest</li>
					<li>BONUS: Deposit your imUSD into the imUSD Vault to earn MTA token rewards</li>
					<li>Explore the numbers behind the APY on <a href='https://app.mstable.org/analytics' target='_blank'>mStable.org/analytics</a></li>
        </ol>
      </div>
      
