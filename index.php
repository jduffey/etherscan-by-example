        <h2 id='play-no-loss-lottery'>Try: Play a no-loss lottery 🎟</h2>
				<p class='center'>Enter to win all the interest earned on the pool<p>
        <ol>
          <li>Go to <a href='https://app.pooltogether.com/' target='_blank'>app.pooltogether.com</a> in Chrome (with MetaMask)</li>
					<li>Click Account, click Connect Wallet, click Meta Mask, confirm any popups</li>
					<li>Click Pools and click Get tickets</li>
					<li>Enter number of tickets. (Need DAI? <a href='#swap-on-uniswap'>Swap for some on Uniswap</a>) Click continue.</li>
					<li>Here are 2 transactions to confirm: Allow DAI and then Deposit</li>
					<li>All set! Weekly drawings are Friday evenings. Good luck!</li>
        </ol>
