        <h2 id='must-knows'>The must-knows 💡</h2>
        <ul>
          <li>Always try things with small amounts of money before using larger amounts</li>
          <li>You need ether (ETH) to pay for gas to transact on the Ethereum network</li>
          <li>There are gas fees on transactions that will negate any positive yield in DeFi when using small amounts of money, but it is a safe way to learn before using larger amounts of money</li>
					<li>Make sure network <a href='#check-gas-prices'>gas prices</a> aren't too high for trying things</li>
					<li>Never share your seed phrase or store them on your phone or computer. Write it down on paper!</li>
          <li>Only send ETH to an ETH address, bitcoin to a bitcoin addresses, etc!</li>
          <li>However, you can send and receive <a href='https://etherscan.io/tokens' target='_blank'>ERC20 tokens</a> with an Ethereum address since they are programmed on the Ethereum network</li>
        </ul>
      </div>
      
