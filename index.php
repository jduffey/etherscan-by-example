				<p class='center'>Connect or enter your wallet address to get info on your liquidity positions<p>
        <ol>
					<li>Go to <a href='https://apy.vision/' target='_blank'>APY.Vision</a></li>
					<li>Scroll to find and click Connect Wallet</li>
					<li>Choose Meta Mask, confirm popup</li>
