      <div class='content'>
        <h2 id='check-gas-prices'>Check gas prices ⛽</h2>
				<p class='center'>The network can be busy and cost too much to try things with small amounts<p>
        <ol>
					<li>Go to <a href='https://www.gasnow.org/' target='_blank'>GasNow.org</a> or <a href='https://ethgas.app/' target='_blank'>EthGas.app</a></li>
					<li>This shows gas prices for how fast you want your transactions to be confirmed</li>
					<li>If all numbers are above 40 or 50, wait for another time when the network is less busy</li>
        </ol>
      </div>
      
