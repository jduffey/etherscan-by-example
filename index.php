      <div class='content'>
        <h2 id='supply-to-uniswap'>Try: Supply liquidity and earn fees on Uniswap 🦄</h2>
				<p class='center'>Supply liquidity to a trading pool and earn the fees from people paying to swap<p>
        <ol>
					<li>Understand what ⚠️<a href='https://finematics.com/impermanent-loss-explained/' target='_blank'>Impermanent Loss</a> ⚠️ is first</li>
					<li>We will put 2 tokens of equal total USD value into a 50/50 pool for others to swap between</li>
					<li>A good resource to see which pools are earning the most fees: <a href='https://info.uniswap.org/pairs' target='_blank'>info.uniswap.org/pairs</a></li>
					<li>A good pool that earns decent APY from fees is the USDC-ETH pool</li>
					<li>If you have, for example, $50 of USDC and $50 of ETH, you can try the next step</li>
					<li>Go to <a href='https://app.uniswap.org/#/add/0xa0b86991c6218b36c1d19d4a2e9eb0ce3606eb48/ETH' target='_blank'>app.uniswap.org/add</a></li>
					<li>Enter $50 for USDC and ETH should prefill with $50 worth of ETH</li>
					<li>Click to Approve (for USDC), then click Supply. Done!</li>
					<li>If you don't have 2 tokens to supply, you can <a href='#swap-on-uniswap'>swap for what you need first on Uniswap</a></li>
					<li>Track your liquidity position on <a href='#track-your-portfolio'>Zapper.fi</a> 📊and <a href='#track-il'>APY.Vision</a> 👀</li>
        </ol>
      </div>
      
