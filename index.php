      <div class='content'>
        <h2 id='swap-on-uniswap'>Try: Swap tokens on Uniswap 🦄</h2>
				<p class='center'>Swap a token for another token straight from your wallet<p>
        <ol>
          <li>Go to <a href='https://app.uniswap.org/#/swap' target='_blank'>app.uniswap.org/#/swap</a> in Chrome (with MetaMask extension ready to go)</li>
          <li>Click connect wallet, choose MetaMask wallet, click connect</li>
          <li>Select ETH as the from token</li>
          <li>Select DAI, or any token you'd like, as the to token</li>
          <li>Type in amount of ETH (perhaps 0.01)</li>
          <li>Click to approve new tokens (one time approval per token)</li>
          <li>Click swap and approve transaction</li>
          <li>When transaction is complete, token will be in your wallet</li>
        </ol>
      </div>
      
