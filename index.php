        <h2 id='what-is-defi'>What is DeFi 🤔</h2>
        <ul>
          <li class='i'>Short for Decentralized Finance</li>
          <li class='i'>Financial products and services built as immutable verifiable code</li>
					<li class='i'>Most of DeFi is built on the Ethereum blockchain</li>
					<li class='i'>No intermediaries or middle-men</li>
					<li class='i'>Available to anyone in the world with an internet connection</li>
        </ul>
      </div>
      
