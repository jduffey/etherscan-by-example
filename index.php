        <h2 id='buy-dpi'>Try: Invest in DeFi with a single token - DPI 💲</h2>
				<p class='center'>DeFi Pulse Index (DPI) is a capitalization-weighted index that tracks top DeFi tokens<p>
        <ol>
					<li>Go to <a href='https://www.coinbase.com/' target='_blank'>Coinbase</a> and buy dollar amount of ETH you want of DPI (add $2-$5 for gas)</li>
					<li>Once the ETH is available to send off Coinbase, send it to your <a href='#get-a-wallet'>wallet</a></li>
					<li>Go to <a href='https://app.uniswap.org/#/swap?inputCurrency=ETH&outputCurrency=0x1494ca1f11d487c2bbe4543e90080aeba4ba3c2b' target='_blank'>Uniswap</a> 🦄 to swap the ETH for DPI</li>
					<li>Enter amount of ETH to swap for DPI, click swap, confirm transaction</li>
					<li>Done. DPI will be in your wallet once transaction is confirmed</li>
					<li>Information on the DPI token: <a href='https://www.tokensets.com/portfolio/dpi' target='_blank'>TokenSets.com/portfolio/dpi</a></li>
