      <div class='content'>
        <h2 id='earn-on-compound'>Try: Earn interest on Compound 📈</h2>
				<p class='center'>Supply assets to the protocol and earn interest. No minimum. Withdraw anytime.<p>
        <ol>
          <li>Go to <a href='https://app.compound.finance' target='_blank'>app.compound.finance</a> in Chrome (with MetaMask extension ready)</li>
          <li>Click to use MetaMask wallet, approve popup</li>
					<li>Notice the Supply Markets APY for lending particular assets</li>
					<li>Take special notice of stablecoin markets such as USDC, DAI, and USDT</li>
					<li>Stablecoins are designed to always be worth $1 so you can consider them US Dollars in crypto</li>
					<li>Click on an asset to supply (perhaps you need to <a href='#supply-to-uniswap'>swap</a> for the one you want to supply)</li>
          <li>Type in amount you wish to supply</li>
					<li>You may have to click Enable for that asset first, confirm that transaction</li>
          <li>Click Supply and then confirm the transaction</li>
					<li>Done. <a href="https://compound.finance/documents/Compound.Whitepaper.pdf" target='_blank'>Read about how compound.finance works</a></li>
					<li>Or take a short lesson and <a href="https://coinbase.com/earn/comp/invite/5nkp6vrj" target='_blank'>earn $10 of $COMP tokens</a> from Coinbase</li>
        </ol>
      </div>
      
