        <h2 id='get-a-wallet'>First: Get a wallet 💰</h2>
				<p class='center'>Start here. You need a wallet to interact with DeFi protocols.<p>
				
				<ol>
					<li>Get a <a href='https://www.coinbase.com/join/davis_95' target='_blank'>Coinbase</a> account</li>
          <li>Connect your checking account</li>
          <li>Buy $10-$50 worth of Ethereum (make take some days for funds to clear bank)</li>
					<li>Download <a href="https://metamask.io/download.html" target='_blank'>MetaMask mobile app</a> (DeFi on mobile)</li>
					<li>Choose Create a new wallet</li>
					<li>Follow on-screen instructions, write down seed phrase on paper</li>
					<li>Download <a href="https://metamask.io/download.html" target="_blank">MetaMask Google Chrome extension</a> (DeFi on desktop)</li>
          <li>Choose restore wallet from seed phrase (same one from MetaMask mobile)</li>
					<li>Once ETH is cleared to send off Coinbase, send to your wallet address</li>
					<li>To get address: touch Add Funds on mobile or click account name in Chrome extension</li>
				</ol>
        <p class='center'>Now you are ready to try things.</p>
      </div>
      
